package bz.sunlight.domain.business;

public class SparePartDemo {

		private String partId;				// 备件id
		private String partCode;			// 备件编号
		private String partName;			// 备件名称
		private String brandName;			// 备件品牌名称
		private String icon;				// 备件图标的绝对地址
		
		public String getPartId() {
			return partId;
		}
		public void setPartId(String partId) {
			this.partId = partId;
		}
		public String getPartCode() {
			return partCode;
		}
		public void setPartCode(String partCode) {
			this.partCode = partCode;
		}
		public String getPartName() {
			return partName;
		}
		public void setPartName(String partName) {
			this.partName = partName;
		}
		public String getBrandName() {
			return brandName;
		}
		public void setBrandName(String brandName) {
			this.brandName = brandName;
		}
		public String getIcon() {
			return icon;
		}
		public void setIcon(String icon) {
			this.icon = icon;
		}
		
}
